    public void uploadSourceCode() throws IOException

    {
      
        String filename = langugagecombobox.getSelectedItem().toString()+".txt"; 
        boolean found = fileFound(filename) ; 
        if(found ==true )
        {
           
        BufferedWriter writer = new BufferedWriter( new FileWriter(filename,true))  ;         
         //FileWriter fw=new FileWriter(firstname+ " "+lastname+".txt");    
         writer.newLine();   //Add new line
         writer.newLine(); 
         String code = codearea.getText();         
        writer.write(code);  
         writer.close();
        }
        else 
        {
             FileWriter fw=new FileWriter(filename); 
             String code = codearea.getText();   
             fw.write(code) ; 
             fw.close(); 
        }
        System.out.println(found);
    }
   
